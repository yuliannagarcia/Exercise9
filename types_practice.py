# say_my_name.py

# Step 1: Create two variables containing your first name and last name
first_name = "Yulianna"
last_name = "Garcia"

# Step 2: Print full name with a space between first and last name using concatenation
print(first_name + " " + last_name)

# Step 3: Transfer variable values into a list and display the list
name_list = [first_name, last_name]
print(name_list)

# Step 4: Store the values in a dictionary using keys 'first' and 'last', and display the dictionary values
name_dict = {'first': first_name, 'last': last_name}
print(name_dict)
