# Two variables containing first name and last name
first_name = "Yulianna"
last_name = "Garcia"

# Step 2: Print full name with a space between first and last name using concatenation
print(first_name + " " + last_name)

# Variables values transferred into a list and print the list
name_list = [first_name, last_name]
print(name_list)

# Values stored in a dictionary using keys 'first' and 'last', and print the dictionary values
name_dict = {'first': first_name, 'last': last_name}
print(name_dict)
